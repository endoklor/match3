class Частица:
    def __init__(self, x: float, y: float, цвет: tuple):
        self.x = x
        self.y = y
        скорость = random.uniform(2.0, 7.0)
        угол = random.uniform(0, 2 * math.pi)
        self.vx = скорость * math.cos(угол)
        self.vy = скорость * math.sin(угол)
        self.жизнь = random.randint(25, 50)
        self.макс_жизнь = self.жизнь
        self.цвет = цвет
        self.размер = random.randint(3, 7)

    def обновить(self):
        self.x += self.vx
        self.y += self.vy
        self.vy += 0.25   # гравитация
        self.жизнь -= 1

    def нарисовать(self, экран: pygame.Surface):
        if self.жизнь <= 0:
            return
        альфа = int(255 * self.жизнь / self.макс_жизнь)
        s = pygame.Surface((self.размер * 2, self.размер * 2), pygame.SRCALPHA)
        pygame.draw.circle(s, (*self.цвет, альфа),
                           (self.размер, self.размер), self.размер)
        экран.blit(s, (int(self.x - self.размер), int(self.y - self.размер)))

    @property
    def жив(self):
        return self.жизнь > 0


class СистемаЧастиц:
    def __init__(self):
        self.частицы: list[Частица] = []

    def испустить(self, x: float, y: float, цвет: tuple, кол: int = 12):
        for _ in range(кол):
            self.частицы.append(Частица(x, y, цвет))

    def обновить(self):
        self.частицы = [p for p in self.частицы if p.жив]
        for p in self.частицы:
            p.обновить()

    def нарисовать(self, экран: pygame.Surface):
        for p in self.частицы:
            p.нарисовать(экран)


