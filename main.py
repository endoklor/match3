class Доска:
    """
    Управляет сеткой 8x8: поиск совпадений, гравитация, бонусы.
    """

    def __init__(self):
        self.сетка: list[list[Плитка | None]] = []
        self._инициализировать()

    def _инициализировать(self):
        self.сетка = [[None] * КОЛОНОК for _ in range(СТРОК)]
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                self.сетка[r][c] = self._создать_плитку(r, c, избежать=True)

    def _случайный_тип(self) -> int:
        return random.randint(0, len(ЦВЕТА_САМОЦ) - 1)

    def _создать_плитку(self, строка: int, колонка: int,
                         избежать: bool = False) -> Плитка:
        """Создаёт плитку, опционально избегая начальных совпадений."""
        попыток = 0
        while True:
            тип = self._случайный_тип()
            if избежать and попыток < 25:
                # Проверка слева
                if (колонка >= 2
                        and self.сетка[строка][колонка - 1] is not None
                        and self.сетка[строка][колонка - 2] is not None
                        and self.сетка[строка][колонка - 1].тип == тип
                        and self.сетка[строка][колонка - 2].тип == тип):
                    попыток += 1
                    continue
                # Проверка выше
                if (строка >= 2
                        and self.сетка[строка - 1][колонка] is not None
                        and self.сетка[строка - 2][колонка] is not None
                        and self.сетка[строка - 1][колонка].тип == тип
                        and self.сетка[строка - 2][колонка].тип == тип):
                    попыток += 1
                    continue
            break
        return Плитка(тип, строка, колонка)

    # ── Вспомогательные методы ──

    def получить(self, r: int, c: int) -> "Плитка | None":
        if 0 <= r < СТРОК and 0 <= c < КОЛОНОК:
            return self.сетка[r][c]
        return None

    def обменять(self, r1: int, c1: int, r2: int, c2: int):
        """Меняет две плитки местами, обновляя их позиции и цели."""
        a = self.сетка[r1][c1]
        b = self.сетка[r2][c2]
        self.сетка[r1][c1] = b
        self.сетка[r2][c2] = a
        if a:
            a.строка, a.колонка = r2, c2
            a.обновить_цель()
            a.падает = True
        if b:
            b.строка, b.колонка = r1, c1
            b.обновить_цель()
            b.падает = True

    # ── Поиск совпадений ──

    def найти_совпадения(self) -> set:
        """Возвращает множество (строка, колонка) всех совпадений."""
        совпали = set()

        # По горизонтали
        for r in range(СТРОК):
            c = 0
            while c < КОЛОНОК:
                плитка = self.получить(r, c)
                if плитка is None:
                    c += 1
                    continue
                серия = [c]
                while (c + 1 < КОЛОНОК
                       and self.получить(r, c + 1) is not None
                       and self.получить(r, c + 1).тип == плитка.тип):
                    c += 1
                    серия.append(c)
                if len(серия) >= 3:
                    for col in серия:
                        совпали.add((r, col))
                    # Назначение бонуса
                    середина = серия[len(серия) // 2]
                    if len(серия) >= 5:
                        self.сетка[r][середина].бонус = БОНУС_ЦВЕТ
                    elif len(серия) == 4:
                        self.сетка[r][середина].бонус = БОНУС_ЛИНИЯ
                c += 1

        # По вертикали
        for c in range(КОЛОНОК):
            r = 0
            while r < СТРОК:
                плитка = self.получить(r, c)
                if плитка is None:
                    r += 1
                    continue
                серия = [r]
                while (r + 1 < СТРОК
                       and self.получить(r + 1, c) is not None
                       and self.получить(r + 1, c).тип == плитка.тип):
                    r += 1
                    серия.append(r)
                if len(серия) >= 3:
                    for row in серия:
                        совпали.add((row, c))
                    середина = серия[len(серия) // 2]
                    if len(серия) >= 5:
                        self.сетка[середина][c].бонус = БОНУС_ЦВЕТ
                    elif len(серия) == 4:
                        self.сетка[середина][c].бонус = БОНУС_ЛИНИЯ
                r += 1

        return совпали

    def расширить_бонусами(self, ячейки: set) -> set:
        """Добавляет к множеству ячейки, поражённые бонусами."""
        дополнительные = set()
        для_цвет_бонуса: set[int] = set()

        for (r, c) in ячейки:
            плитка = self.получить(r, c)
            if плитка is None:
                continue

            if плитка.бонус == БОНУС_ВЗРЫВ:
                # 3x3 вокруг
                for dr in range(-1, 2):
                    for dc in range(-1, 2):
                        nr, nc = r + dr, c + dc
                        if 0 <= nr < СТРОК and 0 <= nc < КОЛОНОК:
                            дополнительные.add((nr, nc))

            elif плитка.бонус == БОНУС_ЛИНИЯ:
                # Очищаем строку И столбец
                for dc in range(КОЛОНОК):
                    дополнительные.add((r, dc))
                for dr in range(СТРОК):
                    дополнительные.add((dr, c))

            elif плитка.бонус == БОНУС_ЦВЕТ:
                # Запоминаем тип - уничтожим все такие
                для_цвет_бонуса.add(плитка.тип)

        # Применяем цвет-бонус
        for тип in для_цвет_бонуса:
            for r in range(СТРОК):
                for c in range(КОЛОНОК):
                    п = self.получить(r, c)
                    if п and п.тип == тип:
                        дополнительные.add((r, c))

        return ячейки | дополнительные

    def пометить_уничтожаемые(self, ячейки: set):
        for (r, c) in ячейки:
            плитка = self.получить(r, c)
            if плитка:
                плитка.уничтожается = True

    def удалить_уничтожаемые(self) -> list:
        """Удаляет плитки с флагом уничтожения. Возвращает список (x, y, тип) для частиц."""
        удалены = []
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                плитка = self.сетка[r][c]
                if плитка and плитка.уничтожается:
                    cx = int(плитка.пиксель_x) + РАЗМЕР_КЛЕТКИ // 2
                    cy = int(плитка.пиксель_y) + РАЗМЕР_КЛЕТКИ // 2
                    удалены.append((cx, cy, плитка.тип))
                    self.сетка[r][c] = None
        return удалены

    def применить_гравитацию(self) -> bool:
        """Опускает плитки вниз в пустые клетки. Возвращает True если что-то изменилось."""
        было_движение = False
        for c in range(КОЛОНОК):
            куда_писать = СТРОК - 1
            for r in range(СТРОК - 1, -1, -1):
                if self.сетка[r][c] is not None:
                    if r != куда_писать:
                        self.сетка[куда_писать][c] = self.сетка[r][c]
                        self.сетка[r][c] = None
                        плитка = self.сетка[куда_писать][c]
                        плитка.строка = куда_писать
                        плитка.обновить_цель()
                        плитка.падает = True
                        было_движение = True
                    куда_писать -= 1
        return было_движение

    def заполнить_пустые(self):
        """Заполняет пустые клетки сверху новыми плитками."""
        for c in range(КОЛОНОК):
            смещение_спавна = -1
            for r in range(СТРОК):
                if self.сетка[r][c] is None:
                    тип = self._случайный_тип()
                    плитка = Плитка(тип, r, c)
                    # Начинаем выше экрана
                    плитка.пиксель_y = (ОТСТУП_ДОСКИ_Y +
                                        смещение_спавна * РАЗМЕР_КЛЕТКИ)
                    плитка.пиксель_x = float(плитка.цель_x)
                    плитка.падает = True
                    self.сетка[r][c] = плитка
                    смещение_спавна -= 1

    def все_на_месте(self) -> bool:
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                п = self.сетка[r][c]
                if п and not п.на_месте:
                    return False
        return True

    def есть_допустимый_ход(self) -> bool:
        """Проверяет, есть ли хотя бы один ход, создающий совпадение."""
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                for dr, dc in [(0, 1), (1, 0)]:
                    nr, nc = r + dr, c + dc
                    if 0 <= nr < СТРОК and 0 <= nc < КОЛОНОК:
                        self.обменять(r, c, nr, nc)
                        совпали = self.найти_совпадения()
                        self.обменять(r, c, nr, nc)
                        if совпали:
                            return True
        return False

    def обновить_анимацию_падения(self):
        """Двигает падающие плитки к цели."""
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                п = self.сетка[r][c]
                if п and п.падает:
                    п.двигаться_к_цели()

    def обновить_анимацию_уничтожения(self) -> bool:
        """Уменьшает и затемняет уничтожаемые плитки. Возвращает True когда всё завершено."""
        всё_готово = True
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                п = self.сетка[r][c]
                if п and п.уничтожается:
                    п.масштаб = max(0.0, п.масштаб - 0.09)
                    п.альфа = max(0, п.альфа - 22)
                    if п.масштаб > 0.05:
                        всё_готово = False
        return всё_готово

    def перемешать(self):
        """Перемешивает доску когда нет доступных ходов."""
        плоский = [self.сетка[r][c] for r in range(СТРОК)
                    for c in range(КОЛОНОК) if self.сетка[r][c]]
        random.shuffle(плоский)
        idx = 0
        for r in range(СТРОК):
            for c in range(КОЛОНОК):
                if idx < len(плоский):
                    плоский[idx].строка = r
                    плоский[idx].колонка = c
                    плоский[idx].обновить_цель()
                    плоский[idx].падает = True
                    self.сетка[r][c] = плоский[idx]
                    idx += 1


