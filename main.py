class Состояние:
    ОЖИДАНИЕ    = "ожидание"
    ОБМЕН       = "обмен"
    УНИЧТОЖЕНИЕ = "уничтожение"
    ПАДЕНИЕ     = "падение"
    ПРОВЕРКА    = "проверка"
    КОНЕЦ       = "конец"
    ПОБЕДА      = "победа"


# ─────────────────────────────────────────
#  ОСНОВНОЙ КЛАСС ИГРЫ
# ─────────────────────────────────────────

class Игра:
    def __init__(self, экран: pygame.Surface):
        self.экран = экран
        self.часы  = pygame.time.Clock()
        self.тик   = 0

        # Шрифты
        self.шрифт_большой  = pygame.font.SysFont("Segoe UI", 44, bold=True)
        self.шрифт_средний  = pygame.font.SysFont("Segoe UI", 28, bold=True)
        self.шрифт_малый    = pygame.font.SysFont("Segoe UI", 18)
        self.шрифт_всплыв   = pygame.font.SysFont("Segoe UI", 22, bold=True)

        # Заранее создаём поверхности самоцветов
        self.поверхности = [
            создать_поверхность_самоцвета(i, РАЗМЕР_КЛЕТКИ - 8)
            for i in range(len(ЦВЕТА_САМОЦ))
        ]

        self.частицы = СистемаЧастиц()
        self.всплывашки: list[ВсплывОчки] = []
        self._сброс()

    # ── Сброс ──

    def _сброс(self):
        self.доска      = Доска()
        self.состояние  = Состояние.ОЖИДАНИЕ
        self.очки       = 0
        self.ходов_осталось = МАКС_ХОДОВ
        self.комбо      = 0
        self.выбрана: tuple | None = None
        self.частицы    = СистемаЧастиц()
        self.всплывашки = []

    # ── Главный цикл ──

    def запустить(self):
        while True:
            self.часы.tick(FPS)
            self.тик += 1
            self._обработать_события()
            self._обновить()
            self._нарисовать()
            pygame.display.flip()

    # ── Обработка событий ──

    def _обработать_события(self):
        for событие in pygame.event.get():
            if событие.type == pygame.QUIT:
                pygame.quit()
                sys.exit()

            if событие.type == pygame.KEYDOWN:
                if событие.key == pygame.K_r:
                    self._сброс()

            if событие.type == pygame.MOUSEBUTTONDOWN and событие.button == 1:
                self._обработать_клик(*событие.pos)

    def _обработать_клик(self, mx: int, my: int):
        # Кнопка рестарта
        if pygame.Rect(ШИРИНА_ЭКРАНА - 148, 32, 128, 46).collidepoint(mx, my):
            self._сброс()
            return

        # На экране конца / победы - перезапуск
        if self.состояние in (Состояние.КОНЕЦ, Состояние.ПОБЕДА):
            self._сброс()
            return

        # Клики разрешены только в режиме ожидания
        if self.состояние != Состояние.ОЖИДАНИЕ:
            return

        # Определяем клетку под курсором
        col = (mx - ОТСТУП_ДОСКИ_X) // РАЗМЕР_КЛЕТКИ
        row = (my - ОТСТУП_ДОСКИ_Y) // РАЗМЕР_КЛЕТКИ

        if not (0 <= row < СТРОК and 0 <= col < КОЛОНОК):
            # Клик вне доски - снимаем выбор
            self._снять_выбор()
            return

        if self.выбрана is None:
            # Первый клик - выбираем плитку
            плитка = self.доска.получить(row, col)
            if плитка:
                self.выбрана = (row, col)
                плитка.выбрана = True
        else:
            sr, sc = self.выбрана
            # Снимаем подсветку со старой
            прежняя = self.доска.получить(sr, sc)
            if прежняя:
                прежняя.выбрана = False

            if (abs(row - sr) == 1 and col == sc) or \
               (abs(col - sc) == 1 and row == sr):
                # Соседняя клетка - пробуем обмен
                self._попытаться_обменять(sr, sc, row, col)
            elif row == sr and col == sc:
                # Та же клетка - снимаем выбор
                pass
            else:
                # Не соседняя - переносим выбор
                новая = self.доска.получить(row, col)
                if новая:
                    self.выбрана = (row, col)
                    новая.выбрана = True
                    return

            self.выбрана = None

    def _снять_выбор(self):
        if self.выбрана is not None:
            sr, sc = self.выбрана
            п = self.доска.получить(sr, sc)
            if п:
                п.выбрана = False
            self.выбрана = None

    def _попытаться_обменять(self, r1: int, c1: int, r2: int, c2: int):
        """Пробует обмен. Если нет совпадений - откат."""
        self.доска.обменять(r1, c1, r2, c2)
        совпали = self.доска.найти_совпадения()
        if совпали:
            self.ходов_осталось -= 1
            self.комбо = 0
            self.состояние = Состояние.ОБМЕН
            # Сбрасываем бонусы (перезаписываются при поиске)
        else:
            # Откат - меняем обратно
            self.доска.обменять(r1, c1, r2, c2)
            # Плавно возвращаем на место (они уже вернулись, но target обновлён)
            for r, c in [(r1, c1), (r2, c2)]:
                п = self.доска.получить(r, c)
                if п:
                    п.обновить_цель()
                    п.падает = True

    # ── Обновление состояния ──

    def _обновить(self):
        self.доска.обновить_анимацию_падения()
        self.частицы.обновить()
        self.всплывашки = [v for v in self.всплывашки if v.жив]
        for v in self.всплывашки:
            v.обновить()

        if self.состояние == Состояние.ОБМЕН:
            # Ждём, пока анимация обмена завершится
            if self.доска.все_на_месте():
                self.состояние = Состояние.ПРОВЕРКА

        elif self.состояние == Состояние.ПРОВЕРКА:
            совпали = self.доска.найти_совпадения()
            if совпали:
                расширено = self.доска.расширить_бонусами(совпали)
                self.доска.пометить_уничтожаемые(расширено)
                self._начислить_очки(расширено)
                self.состояние = Состояние.УНИЧТОЖЕНИЕ
            else:
                self.комбо = 0
                self.состояние = Состояние.ОЖИДАНИЕ
                self._проверить_конец()

        elif self.состояние == Состояние.УНИЧТОЖЕНИЕ:
            готово = self.доска.обновить_анимацию_уничтожения()
            if готово:
                удалены = self.доска.удалить_уничтожаемые()
                for (px, py, тип) in удалены:
                    self.частицы.испустить(px, py, ЦВЕТА_САМОЦ[тип], кол=14)
                self.доска.применить_гравитацию()
                self.доска.заполнить_пустые()
                self.состояние = Состояние.ПАДЕНИЕ

        elif self.состояние == Состояние.ПАДЕНИЕ:
            if self.доска.все_на_месте():
                self.состояние = Состояние.ПРОВЕРКА

    def _начислить_очки(self, ячейки: set):
        self.комбо += 1
        база = len(ячейки) * 55
        итого = база * self.комбо
        self.очки += итого

        cx = ОТСТУП_ДОСКИ_X + КОЛОНОК * РАЗМЕР_КЛЕТКИ // 2
        cy = ОТСТУП_ДОСКИ_Y + СТРОК * РАЗМЕР_КЛЕТКИ // 2
        self.всплывашки.append(ВсплывОчки(cx, cy - 50, итого, self.комбо))

    def _проверить_конец(self):
        if self.очки >= ЦЕЛЬ_ОЧКОВ:
            self.состояние = Состояние.ПОБЕДА
        elif self.ходов_осталось <= 0:
            self.состояние = Состояние.КОНЕЦ
        elif not self.доска.есть_допустимый_ход():
            self.доска.перемешать()
            self.состояние = Состояние.ПАДЕНИЕ

    # ── Отрисовка ──

    def _нарисовать(self):
        self.экран.fill(ФОН)
        self._нарисовать_фон()
        self._нарисовать_доску()
        self._нарисовать_плитки()
        self.частицы.нарисовать(self.экран)
        self._нарисовать_интерфейс()
        self._нарисовать_всплывашки()

        if self.состояние == Состояние.КОНЕЦ:
            self._нарисовать_наложение("КОНЕЦ ИГРЫ",
                                        "Нажмите R или кликните для перезапуска",
                                        КРАСНЫЙ)
        elif self.состояние == Состояние.ПОБЕДА:
            self._нарисовать_наложение("ПОБЕДА!",
                                        "Нажмите R или кликните для перезапуска",
                                        ЗОЛОТОЙ)

