class ВсплывОчки:
    def __init__(self, x: float, y: float, значение: int, комбо: int = 1):
        self.x = x
        self.y = y
        self.значение = значение
        self.комбо = комбо
        self.жизнь = 70
        self.макс_жизнь = 70

    def обновить(self):
        self.y -= 1.2
        self.жизнь -= 1

    def нарисовать(self, экран: pygame.Surface, шрифт: pygame.font.Font):
        if self.жизнь <= 0:
            return
        альфа = int(255 * self.жизнь / self.макс_жизнь)
        текст = f"+{self.значение}"
        if self.комбо > 1:
            текст += f"   x{self.комбо} COMBO!"
        цвет = ЗОЛОТОЙ if self.комбо > 1 else БЕЛЫЙ
        надпись = шрифт.render(текст, True, цвет)
        s = pygame.Surface(надпись.get_size(), pygame.SRCALPHA)
        s.blit(надпись, (0, 0))
        s.set_alpha(альфа)
        экран.blit(s, (int(self.x - надпись.get_width() // 2), int(self.y)))

    @property
    def жив(self):
        return self.жизнь > 0


