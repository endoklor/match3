class Плитка:
    """
    Один самоцвет на доске.
    Хранит позицию в сетке и текущую пиксельную позицию для анимации.
    """

    def __init__(self, тип_самоцвета: int, строка: int, колонка: int):
        self.тип = тип_самоцвета
        self.строка = строка
        self.колонка = колонка
        self.бонус = БЕЗ_БОНУСА

        # Целевая позиция (куда должна прийти)
        self.цель_x = колонка * РАЗМЕР_КЛЕТКИ + ОТСТУП_ДОСКИ_X
        self.цель_y = строка * РАЗМЕР_КЛЕТКИ + ОТСТУП_ДОСКИ_Y

        # Текущая пиксельная позиция
        self.пиксель_x = float(self.цель_x)
        self.пиксель_y = float(self.цель_y)

        # Анимационные параметры
        self.альфа = 255
        self.масштаб = 1.0
        self.уничтожается = False
        self.падает = False
        self.выбрана = False

        # Плавное покачивание (offset фазы)
        self.фаза_покачивания = random.uniform(0, 2 * math.pi)

    def обновить_цель(self):
        """Пересчитать целевую позицию по строке и колонке."""
        self.цель_x = self.колонка * РАЗМЕР_КЛЕТКИ + ОТСТУП_ДОСКИ_X
        self.цель_y = self.строка * РАЗМЕР_КЛЕТКИ + ОТСТУП_ДОСКИ_Y

    def двигаться_к_цели(self, скорость: float = СКОРОСТЬ_ПАДЕНИЯ):
        """Плавно двигать к целевой позиции. Возвращает True, если достигли."""
        dx = self.цель_x - self.пиксель_x
        dy = self.цель_y - self.пиксель_y

        if abs(dx) <= скорость:
            self.пиксель_x = self.цель_x
        else:
            self.пиксель_x += скорость * (1 if dx > 0 else -1)

        if abs(dy) <= скорость:
            self.пиксель_y = self.цель_y
        else:
            self.пиксель_y += скорость * (1 if dy > 0 else -1)

        достигли = (self.пиксель_x == self.цель_x and
                    self.пиксель_y == self.цель_y)
        if достигли:
            self.падает = False
        return достигли

    @property
    def на_месте(self) -> bool:
        return (self.пиксель_x == self.цель_x and
                self.пиксель_y == self.цель_y)

    def нарисовать(self, экран: pygame.Surface,
                    поверхности: list, тик: int):
        if self.альфа <= 0:
            return

        базовая = поверхности[self.тип]
        размер = int(РАЗМЕР_КЛЕТКИ * self.масштаб)
        if размер <= 2:
            return

        # Лёгкое покачивание вверх-вниз (только когда на месте)
        покач_y = 0.0
        if not self.падает and not self.уничтожается:
            покач_y = math.sin(тик * 0.035 + self.фаза_покачивания) * 2.0

        # Масштабирование поверхности
        if размер != базовая.get_width():
            отрисов = pygame.transform.smoothscale(базовая, (размер, размер))
        else:
            отрисов = базовая

        # Прозрачность
        if self.альфа < 255:
            отрисов = отрисов.copy()
            отрисов.set_alpha(self.альфа)

        draw_x = int(self.пиксель_x + (РАЗМЕР_КЛЕТКИ - размер) // 2)
        draw_y = int(self.пиксель_y + (РАЗМЕР_КЛЕТКИ - размер) // 2 + покач_y)

        # Рамка выбора
        if self.выбрана:
            рамка = pygame.Surface((РАЗМЕР_КЛЕТКИ + 6, РАЗМЕР_КЛЕТКИ + 6), pygame.SRCALPHA)
            pygame.draw.rect(рамка, (255, 245, 80, 200),
                             (0, 0, РАЗМЕР_КЛЕТКИ + 6, РАЗМЕР_КЛЕТКИ + 6),
                             border_radius=12, width=3)
            экран.blit(рамка, (int(self.пиксель_x) - 3,
                                int(self.пиксель_y + покач_y) - 3))

        экран.blit(отрисов, (draw_x, draw_y))

        # Значок бонуса
        if self.бонус == БОНУС_ЛИНИЯ:
            self._нарисовать_значок(экран, draw_x, draw_y, размер,
                                     "L", (80, 220, 255))
        elif self.бонус == БОНУС_ВЗРЫВ:
            self._нарисовать_значок(экран, draw_x, draw_y, размер,
                                     "B", (255, 90, 90))
        elif self.бонус == БОНУС_ЦВЕТ:
            self._нарисовать_значок(экран, draw_x, draw_y, размер,
                                     "C", (255, 240, 80))

    @staticmethod
    def _нарисовать_значок(экран, x, y, размер,
                             буква: str, цвет: tuple):
        шрифт = pygame.font.SysFont("Arial", 13, bold=True)
        значок = pygame.Surface((22, 22), pygame.SRCALPHA)
        pygame.draw.circle(значок, (*цвет, 230), (11, 11), 11)
        txt = шрифт.render(буква, True, (10, 10, 10))
        значок.blit(txt, (11 - txt.get_width() // 2,
                           11 - txt.get_height() // 2))
        экран.blit(значок, (x + размер - 14, y))


