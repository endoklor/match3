def создать_поверхность_самоцвета(тип: int, размер: int) -> pygame.Surface:
    """Создаёт красивую поверхность самоцвета нужного типа."""
    surf = pygame.Surface((размер, размер), pygame.SRCALPHA)
    цвет = ЦВЕТА_САМОЦ[тип]
    cx, cy = размер // 2, размер // 2
    r = размер // 2 - 5

    формы = ["circle", "square", "diamond", "hexagon",
             "triangle", "star", "cross", "rounded_square"]
    форма = формы[тип % len(формы)]

    # Тень
    тень = pygame.Surface((размер, размер), pygame.SRCALPHA)
    pygame.draw.circle(тень, (0, 0, 0, 60), (cx + 2, cy + 3), r)
    surf.blit(тень, (0, 0))

    # Основная фигура
    если_темнее = tuple(max(0, c - 50) for c in цвет)
    если_светлее = tuple(min(255, c + 70) for c in цвет)

    if форма == "circle":
        pygame.draw.circle(surf, если_темнее, (cx, cy), r)
        pygame.draw.circle(surf, цвет, (cx, cy), r - 2)

    elif форма == "square":
        rect = pygame.Rect(5, 5, размер - 10, размер - 10)
        pygame.draw.rect(surf, если_темнее, rect, border_radius=8)
        pygame.draw.rect(surf, цвет, rect.inflate(-4, -4), border_radius=6)

    elif форма == "diamond":
        pts = [(cx, 5), (размер - 5, cy), (cx, размер - 5), (5, cy)]
        pygame.draw.polygon(surf, если_темнее, pts)
        inner = [(cx, 10), (размер - 10, cy), (cx, размер - 10), (10, cy)]
        pygame.draw.polygon(surf, цвет, inner)

    elif форма == "hexagon":
        pts = []
        for i in range(6):
            угол = math.radians(60 * i - 30)
            pts.append((cx + r * math.cos(угол), cy + r * math.sin(угол)))
        pygame.draw.polygon(surf, если_темнее, pts)
        inner_pts = []
        for i in range(6):
            угол = math.radians(60 * i - 30)
            inner_pts.append((cx + (r - 4) * math.cos(угол),
                               cy + (r - 4) * math.sin(угол)))
        pygame.draw.polygon(surf, цвет, inner_pts)

    elif форма == "triangle":
        pts = [(cx, 5), (размер - 5, размер - 5), (5, размер - 5)]
        pygame.draw.polygon(surf, если_темнее, pts)
        inner = [(cx, 12), (размер - 12, размер - 12), (12, размер - 12)]
        pygame.draw.polygon(surf, цвет, inner)

    elif форма == "star":
        внеш, внутр = r, r // 2
        pts = []
        for i in range(10):
            угол = math.radians(36 * i - 90)
            rad = внеш if i % 2 == 0 else внутр
            pts.append((cx + rad * math.cos(угол), cy + rad * math.sin(угол)))
        pygame.draw.polygon(surf, если_темнее, pts)
        pts2 = []
        for i in range(10):
            угол = math.radians(36 * i - 90)
            rad = (внеш - 3) if i % 2 == 0 else (внутр - 2)
            pts2.append((cx + rad * math.cos(угол), cy + rad * math.sin(угол)))
        pygame.draw.polygon(surf, цвет, pts2)

    elif форма == "cross":
        w = размер // 3
        pygame.draw.rect(surf, если_темнее, (cx - w // 2 - 1, 4, w + 2, размер - 8))
        pygame.draw.rect(surf, если_темнее, (4, cy - w // 2 - 1, размер - 8, w + 2))
        pygame.draw.rect(surf, цвет, (cx - w // 2, 5, w, размер - 10))
        pygame.draw.rect(surf, цвет, (5, cy - w // 2, размер - 10, w))

    elif форма == "rounded_square":
        rect = pygame.Rect(5, 5, размер - 10, размер - 10)
        pygame.draw.rect(surf, если_темнее, rect, border_radius=16)
        pygame.draw.rect(surf, цвет, rect.inflate(-4, -4), border_radius=14)

    # Блик (глянец)
    блик = pygame.Surface((размер, размер), pygame.SRCALPHA)
    pygame.draw.ellipse(блик, (255, 255, 255, 70),
                        (cx - r // 2, cy - r // 2, r, r // 2))
    surf.blit(блик, (0, 0))

    return surf


